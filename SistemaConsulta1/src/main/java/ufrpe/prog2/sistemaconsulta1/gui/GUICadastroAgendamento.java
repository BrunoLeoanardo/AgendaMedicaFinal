    private void jButtonAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgendarActionPerformed
        // TODO add your handling code here:
        String nomeMedico = jTextFieldAgendarMedico.getText().trim();
        String nomePaciente = jTextFieldAgendarPaciente.getText().trim();
        String data = jTextFieldAgendarData.getText().trim();


        if(nomeMedico.isEmpty() || nomePaciente.isEmpty() || data.isEmpty()){
            System.out.println("Preencha todos os campos!");
            return;
        }

        Fachada f = Fachada.getInstance();

        try {
            Agendamento ag = f.criarAgendamento(nomeMedico, nomePaciente, data);
            JOptionPane.showMessageDialog(this, "Agendamento criado com sucesso!");
            this.dispose(); // fecha janela (opcional)
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonAgendarActionPerformed

