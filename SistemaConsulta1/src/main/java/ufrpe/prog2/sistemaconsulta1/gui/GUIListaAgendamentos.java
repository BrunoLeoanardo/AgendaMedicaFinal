    // MÃ©todo para carregar os agendamentos da Fachada na JList
    private void carregarAgendamentos() {
        DefaultListModel<String> modeloLista = new DefaultListModel<>();
        Fachada fachada = Fachada.getInstance();
        List<Agendamento> agendamentos = fachada.listarAgendamentos();

        for (Agendamento a : agendamentos) {
            modeloLista.addElement(a.toString()); // usa o toString() do Agendamento
        }

        jListListaAgendamentos.setModel(modeloLista);
    }

