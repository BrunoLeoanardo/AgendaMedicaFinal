    // MÃ©todo para carregar os pacientes da Fachada na JList
    private void carregarPacientes() {
        DefaultListModel<String> modeloLista = new DefaultListModel<>();
        Fachada fachada = Fachada.getInstance();
        List<Paciente> pacientes = fachada.listarPacientes();

        for (Paciente p : pacientes) {
            modeloLista.addElement(p.getNome() + " | CPF: " + p.getCpf() + " | Email: " + p.getEmail());
        }

        jListListaPacientes.setModel(modeloLista);
    }

